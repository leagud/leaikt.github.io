Åndsverksloven
Denne loven gir eierskap til personen som har skapt, investerer og/eller fremfører sine verk. Hvis noen andre bruker verket må vedkommende spørre om lov og få godtatt forespørselen, hvis ikke kan det bli straff med bøter eller fengsel inntil tre måneder.

Hvis noen bruker et verk en ikke eier, må man skrive all informasjon om hvem som har skapt verket og i tillegg at man har fått tillatelse å bruke det.

Forskrift om universell utforming av IKT-løsninger i Norge må være universelt utformet. Private og offentlige bedrifter, lag og organisasjoner må følge dette regelverket.
Utforming og tilrettelegging av IKT har blitt laget slik at funksjonen kan benyttes av flest mulig folk. 
