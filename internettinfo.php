Hvordan fungerer internettet? 
Internett er det globale systemet av sammenhengende nettverk mellom datamaskiner som bruker internettprotokollen til å kommunisere mellom forskjellige nettverk og enheter. Det er et gedigent nettverk som består av private, offentlige, forretning, akademisk og myndighetsnettverk fra lokal til global scope. Alt dette er knyttet til et bredt spekter av elektronikk, trådløs og optisk nettverksteknologi.
Ettersom alt om internett dreier seg om nettverk, er det umulig å komme utenom TCP/IP. Det står for Transmission Control Protocol/Internet Protocol og dette brukes til å koble sammen datamaskiner i nettverk, bl.a. Internettet. Dette er de to viktigste kommunikasjonsprotokollene. Uten TCP/IP hadde vi nok ikke hatt Internett som vi kjenner det i dag. Den sørger for at to datamaskiner kan snakke med hverandre over Internett ved å sende data til IP-adressen som med IPv4 betegnes som et 32-bit nummer. Et eksempel er 194.348.15.141. Internettet fungerer ved at en da
amaskin sender en melding til en annen datamaskin sin IP adresse. Meldingen blir delt opp i flere biter som vi kaller for Pakker. Pakkene blir sendt til riktig IP adresse, og underlagt TCP for å opprettholde kvaliteten. Så blir pakkene koblet sammen igjen ved mottakers IP adresse.
